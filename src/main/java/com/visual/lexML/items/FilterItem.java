package com.visual.lexML.items;

import com.visual.lexML.collections.Filter;

public class FilterItem {

	private String field;
	private String value;
	private Filter type;
	
	public String getField() {
		return field;
	}
	public void setField(String field) {
		this.field = field;
	}
	public Filter getType() {
		return type;
	}
	public void setType(Filter type) {
		this.type = type;
	}
	
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	
	public FilterItem(String value, Filter type) {
		super();
		this.value = value;
		this.type = type;
	}
	
	public FilterItem(String field, String value) {
		super();
		this.field = field;
		this.value = value;
		this.type = Filter.OUTRO;
	}
	
	@Override
	public String toString() {
		return "FilterItem [field=" + field + ", value=" + value + ", type=" + type + "]";
	}
	
	
	
	
	
}
